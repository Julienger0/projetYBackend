    public function findLastMessageBetweenUsers(int $userId, int $targetUserId): ?Message
    {
        $qb = $this->createQueryBuilder('m');
    
        $qb->where('m.idSender = :userId AND m.idReceiver = :targetUserId')
           ->orWhere('m.idSender = :targetUserId AND m.idReceiver = :userId')
           ->setParameters([
               'userId' => $userId,
               'targetUserId' => $targetUserId,
           ])
           ->orderBy('m.creationDate', 'DESC')
           ->setMaxResults(1);
    
        return $qb->getQuery()->getOneOrNullResult();
    }

